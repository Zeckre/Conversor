        convertText = Double.valueOf(tempIn.getText());
        temp1 = valueTwo.getSelectedIndex();
        temp2 = valueOne.getSelectedIndex();
        
        
        //Puente
        TemperaturaOperations tempop = new TemperaturaOperations();
        tempop.setTemp1(temp1);
        tempop.setTemp2(temp2);
        
        tempop.setConvertText(convertText);
        
        //Texto de salida
        DecimalFormat df = new DecimalFormat("#.000000");
        tempOut.append("" + df.format(tempop.convertirTemp()));
